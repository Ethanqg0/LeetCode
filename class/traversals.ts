/*
    Pre-order traversal: Root -> left subtree -> right subtree
        This is a depth-first traversal. It is commonly used to copy a tree or save the tree structure.

    In-order traversal: Left subtree -> Root -> Right subtree
        This is a depth-first traversal. It is commonly used to sort a tree and find the smallest or largest element.

    Post-order traversal: Left subtree -> Right subtree -> Root
        This is a depth-first traversal. It is commonly used to delete a tree and evaluate expressions.

    Level-order traversal: Traverse each level from left to right
        This is a breadth-first traversal. It is commonly used to find the shortest path and work from level to level.
*/